class CrossModalityAttention(nn.Module):
    def __init__(self,  hidden_size, modality_relation, attn_dropout=0.0, embed_dropout=0.0, preprocess=False):
        super().__init__()
        self.hidden_size = hidden_size
        # self.num_heads = num_heads
        self.modality_relation = modality_relation
        self.dropout = embed_dropout  # Embedding dropout
        self.attn_dropout = attn_dropout
        self.embed_positions = SinusoidalPositionalEmbedding(hidden_size)

        self.preprocess = preprocess
        self.image_projection = nn.Conv2d(in_channels=3, out_channels=hidden_size, kernel_size=16, stride=16)
        self.text_projection = nn.Embedding(num_embeddings=5597, embedding_dim=256, padding_idx=0)


        # Linear layers to transform the image and text hidden states
        self.query = nn.Linear(hidden_size, hidden_size)
        self.key = nn.Linear(hidden_size, hidden_size)
        self.value = nn.Linear(hidden_size, hidden_size)

        # Linear layer to transform the combined hidden states
        self.combine_projection = nn.Linear(hidden_size * 2, hidden_size)

    def forward(self, image, text):

        # Project the image and text hidden states
        if self.preprocess:
            image_hidden_states = self.image_projection(image)
            text_hidden_states = self.text_projection(text)
        else:
            image_hidden_states = image
            text_hidden_states = text

        if len(image_hidden_states.shape) < 4:
            image_hidden_states = self.image_projection(image_hidden_states)


        bs, c, h, w = image_hidden_states.shape # bs: batchsize, c: channel, h: height, w: width
        image_hidden_states = image_hidden_states.permute(2, 3, 0, 1)
        image_hidden_states = image_hidden_states.view(h * w, bs, c)


        if self.modality_relation == 'vt':
            query = self.query(image_hidden_states)
            key = self.key(text_hidden_states)
            value = self.key(text_hidden_states)
        else: # 'tv'
            query = self.query(text_hidden_states)
            key = self.key(image_hidden_states)
            value = self.key(image_hidden_states)



        # embed tokens and positions

        query += self.embed_positions(query.transpose(0, 1)[:, :, 0]).transpose(0, 1)  # Add positional embedding
        query = F.dropout(query, p=self.dropout, training=self.training)

        if key is not None and value is not None:
            # embed tokens and positions

            if self.embed_positions is not None:
                key += self.embed_positions(key.transpose(0, 1)[:, :, 0]).transpose(0, 1)  # Add positional embedding
                value += self.embed_positions(value.transpose(0, 1)[:, :, 0]).transpose(0, 1)  # Add positional embedding
            key = F.dropout(key, p=self.dropout, training=self.training)
            value = F.dropout(value, p=self.dropout, training=self.training)

        # Calculate the attention weights


        query = query.transpose(0,1)
        attention_weights = torch.matmul(query, key.transpose(1, 2))

        # Normalize the attention weights
        attention_weights = attention_weights / (self.hidden_size ** 0.5)
        attention_weights = nn.Softmax(dim=-1)(attention_weights)

        # Apply the attention weights to the text hidden states
        attended_ = torch.matmul(attention_weights, value)


        # Combine the attended text and the image hidden states
        if self.modality_relation == 'vt':
            combined_hidden_states = torch.cat((attended_, text_hidden_states), dim=-1)
        else:
            # image_hidden_states = image_hidden_states.transpose(0, 1)
            combined_hidden_states = torch.cat((attended_, image_hidden_states), dim=-1)
            combined_hidden_states = combined_hidden_states.transpose(0, 1)

        # Project the combined hidden states
        projected_combined = self.combine_projection(combined_hidden_states)


        return projected_combined






